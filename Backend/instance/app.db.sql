CREATE TABLE IF NOT EXISTS "medicine" (
	"id"	INTEGER NOT NULL,
	"title"	VARCHAR(100) NOT NULL,
	"description"	TEXT NOT NULL,
	"user_id"	INTEGER NOT NULL,
	"image"	VARCHAR(200),
	"created_at"	DATETIME,
	"is_approved"	BOOLEAN,
	"status"	VARCHAR(20),
	PRIMARY KEY("id"),
	FOREIGN KEY("user_id") REFERENCES "users"("id")
);
CREATE TABLE IF NOT EXISTS "medicine_reminder" (
	"id"	INTEGER NOT NULL,
	"user_med_map_id"	INTEGER NOT NULL,
	"reminder_time"	VARCHAR(20) NOT NULL,
	"notification_type"	VARCHAR(20) NOT NULL,
	"message"	VARCHAR(200) NOT NULL,
	"active"	BOOLEAN,
	PRIMARY KEY("id"),
	FOREIGN KEY("user_med_map_id") REFERENCES "user_med_map"("id")
);
CREATE TABLE IF NOT EXISTS "notifications" (
	"id"	INTEGER NOT NULL,
	"sender_id"	INTEGER NOT NULL,
	"receiver_id"	INTEGER NOT NULL,
	"type"	VARCHAR(20) NOT NULL,
	"medicine_id"	INTEGER,
	"slot"	VARCHAR(50),
	"message"	VARCHAR(255) NOT NULL,
	"created_at"	DATETIME NOT NULL,
	"is_read"	BOOLEAN,
	PRIMARY KEY("id"),
	FOREIGN KEY("medicine_id") REFERENCES "medicine"("id"),
	FOREIGN KEY("receiver_id") REFERENCES "users"("id"),
	FOREIGN KEY("sender_id") REFERENCES "users"("id")
);
CREATE TABLE IF NOT EXISTS "status" (
	"id"	INTEGER NOT NULL,
	"user_med_map_id"	INTEGER NOT NULL,
	"date"	DATETIME NOT NULL,
	"breakfast_before"	BOOLEAN,
	"breakfast_after"	BOOLEAN,
	"lunch_before"	BOOLEAN,
	"lunch_after"	BOOLEAN,
	"dinner_before"	BOOLEAN,
	"dinner_after"	BOOLEAN,
	PRIMARY KEY("id"),
	FOREIGN KEY("user_med_map_id") REFERENCES "user_med_map"("id")
);
CREATE TABLE IF NOT EXISTS "user_med_map" (
	"id"	INTEGER NOT NULL,
	"medicine_id"	INTEGER NOT NULL,
	"user_id"	INTEGER NOT NULL,
	"dosage"	VARCHAR NOT NULL,
	"start_date"	DATE NOT NULL,
	"end_date"	DATE,
	"breakfast_before"	BOOLEAN,
	"breakfast_after"	BOOLEAN,
	"lunch_before"	BOOLEAN,
	"lunch_after"	BOOLEAN,
	"dinner_before"	BOOLEAN,
	"dinner_after"	BOOLEAN,
	PRIMARY KEY("id"),
	CONSTRAINT "fk_user_med_map_medicine_id" FOREIGN KEY("medicine_id") REFERENCES "medicine"("id") ON DELETE CASCADE,
	CONSTRAINT "fk_user_med_map_user_id" FOREIGN KEY("user_id") REFERENCES "users"("id")
);
CREATE TABLE IF NOT EXISTS "users" (
	"id"	INTEGER NOT NULL,
	"first_name"	VARCHAR(50) NOT NULL,
	"last_name"	VARCHAR(50) NOT NULL,
	"username"	VARCHAR(50) NOT NULL,
	"role"	VARCHAR(20) NOT NULL,
	"profile_picture"	VARCHAR(200),
	"password_hash"	VARCHAR(128) NOT NULL,
	"created_at"	DATETIME,
	"relation"	VARCHAR(50),
	PRIMARY KEY("id"),
	UNIQUE("username")
);
